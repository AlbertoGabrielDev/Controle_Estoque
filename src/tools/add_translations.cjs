locales.forEach(loc => {
    const file = path.join(basePath, loc + '.json');
    let data = {};
    if (fs.existsSync(file)) {
        data = JSON.parse(fs.readFileSync(file, 'utf-8'));
    }
    
    let added = 0;
    for (const [key, trans] of Object.entries(newTranslations)) {
        if (!data[key]) {
            data[key] = trans[loc];
            added++;
        }
    }
    
    if (added > 0) {
        fs.writeFileSync(file, JSON.stringify(data, null, 4), 'utf-8');
        console.log(`Added ${added} new translations to ${loc}.json`);
    } else {
        console.log(`${loc}.json already up to date.`);
    }
});
